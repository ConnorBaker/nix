// Pattern 6: Multi-Binding Let with 5 Files, All Forced
//
// Models mkDerivation — many bindings from different sources, all forced.
//
// Expression:
//   let
//     a = builtins.readFile <file_a>;
//     b = (builtins.fromJSON (builtins.readFile <file_b>))."key";
//     c = builtins.length (builtins.fromJSON (builtins.readFile <file_c>));
//     d = builtins.readFile <file_d>;
//     e = (builtins.fromJSON (builtins.readFile <file_e>))."ekey";
//   in { inherit a d; value = b; count = c; extra = e; }.a
//
// The attrset construction forces all five bindings even though only .a is
// returned at the outer level.  This means all five file deps are recorded
// on the trace.  Changing any one of them must invalidate the cache (eager
// dep tracking — all bindings forced during attrset construction).
//
// Dep slots:
//   slot[0] = file_a  (Kind::File)
//   slot[1] = file_b  (Kind::JsonFile)
//   slot[2] = file_c  (Kind::JsonArray)
//   slot[3] = file_d  (Kind::File)
//   slot[4] = file_e  (Kind::JsonFile)

