// ── Fixture ──────────────────────────────────────────────────────────────────

class EvalTraceProperty_MultiBindingLet : public TraceCacheFixture {
public:
    EvalTraceProperty_MultiBindingLet() {
        testFingerprint = hashString(HashAlgorithm::SHA256, "prop-multi-binding-let");
    }
};

// ── Helpers ───────────────────────────────────────────────────────────────────

static rc::detail::TestParams makeParams()
{
    auto const & conf = rc::detail::configuration();
    auto params = conf.testParams;
    params.maxSuccess = 50;
    params.maxDiscardRatio = 200;
    return params;
}

// ── Test 1: AccessedBinding_Mutation_Invalidates (RapidCheck) ────────────────
//
// Property: mutating slot[0] (the file backing binding `a`, which is the
// accessed binding at the outer .a access) must invalidate the cache.
//
// Uses RC because slot[0] is always Kind::File (plain readFile — reliable
// invalidation via FileBytes dep, no SC-override path).
//
// RC_PRE guards:
//   depSlots.size() == 5  — generated by makeMultiBindingLetGen
//   depSlots[0].kind == File — slot[0] is always Kind::File for this generator,
//                              but guard defensively for clarity
TEST_F(EvalTraceProperty_MultiBindingLet, AccessedBinding_Mutation_Invalidates)
{
    rc::detail::checkGTestWith(
        [this](TestExpr expr) {
            RC_PRE(expr.depSlots.size() == 5);
            RC_PRE(expr.depSlots[0].kind == DepSlot::Kind::File);

            auto & slot = expr.depSlots[0];

            // Generate a new value that differs from the current one.
            auto newValue = *slot.generateMutation();
            RC_PRE(newValue != slot.currentValue);

            // Cold eval: record trace with all 5 bindings forced.
            {
                auto cache = makeCache(expr.nixCode);
                (void) forceRoot(*cache);
            }

            // Confirm warm cache hit before mutation (precision pre-condition).
            {
                int calls = 0;
                auto cache = makeCache(expr.nixCode, &calls);
                (void) forceRoot(*cache);
                RC_ASSERT(calls == 0);
            }

            // Mutate slot[0] (file_a — the binding accessed at the outer .a).
            slot.mutate(newValue);
            invalidateFileCache(slot.path);

            // Warm eval must re-evaluate: FileBytes dep on file_a changed.
            {
                PathCountersSnapshot snap;
                auto cache = makeCache(expr.nixCode);
                (void) forceRoot(*cache);
                RC_ASSERT(snap.deltaTraceCacheMisses() >= 1);
            }

            // Restore for clean state on shrink/retry.
            slot.restore();
            invalidateFileCache(slot.path);
        },
        makeParams);
}

// ── Test 2: UnaccessedBinding_NoDep_CacheHit (deterministic) ─────────────────
//
// What happens when file_c (the array file for binding `c`) is changed?
//
// Nix is lazy — let bindings are thunks. Only 'a' is forced when '.a' is
// accessed. Bindings b, c, d, e are NEVER forced; the attrset construction
// { inherit a d; value = b; count = c; extra = e; }.a only forces the binding
// needed at the outer access path.  fileC is NOT a dep on the trace.
// Changing file_c does NOT invalidate the cache (calls == 0).
TEST_F(EvalTraceProperty_MultiBindingLet, UnaccessedBinding_NoDep_CacheHit)
{
    // Concrete 5-file setup: file_c is the array file for binding `c`.
    auto fileA = std::make_shared<TempTextFile>("content_a_initial");
    auto fileB = std::make_shared<TempExtFile>("json", R"({"bkey": "hello"})");
    auto fileC = std::make_shared<TempExtFile>("json", "[1, 2, 3]");
    auto fileD = std::make_shared<TempTextFile>("content_d_initial");
    auto fileE = std::make_shared<TempExtFile>("json", R"({"ekey": "world"})");

    std::string nixCode =
        "let\n"
        "  a = builtins.readFile " + fileA->path.string() + ";\n"
        "  b = (builtins.fromJSON (builtins.readFile " + fileB->path.string() + ")).\"bkey\";\n"
        "  c = builtins.length (builtins.fromJSON (builtins.readFile " + fileC->path.string() + "));\n"
        "  d = builtins.readFile " + fileD->path.string() + ";\n"
        "  e = (builtins.fromJSON (builtins.readFile " + fileE->path.string() + ")).\"ekey\";\n"
        "in { inherit a d; value = b; count = c; extra = e; }.a";

    // Cold eval: all 5 bindings are forced during attrset construction.
    {
        auto cache = makeCache(nixCode);
        auto v = forceRoot(*cache);
        EXPECT_EQ(v.type(), nString);
    }

    // Confirm warm cache hit before mutation.
    {
        int calls = 0;
        auto cache = makeCache(nixCode, &calls);
        (void) forceRoot(*cache);
        EXPECT_EQ(calls, 0) << "warm hit expected before any mutation";
    }

    // Mutate file_c (change the integer array) — this is the backing file for
    // binding `c`, which is not directly at the outer .a access path.
    // However, because the attrset construction forces all bindings eagerly,
    // file_c was recorded as a dep.  Changing it must invalidate the cache.
    fileC->modify("[10, 20, 30, 40]");
    invalidateFileCache(fileC->path);

    {
        int calls = 0;
        auto cache = makeCache(nixCode, &calls);
        auto v = forceRoot(*cache);
        // Nix is lazy — let bindings are thunks. Only 'a' is forced when '.a'
        // is accessed. fileC is NOT a dep because binding `c` is never forced.
        EXPECT_EQ(calls, 0)
            << "cache hit expected: Nix lazy evaluation — only 'a' is forced "
               "when '.a' is accessed; fileC is not a dep on the trace";
        EXPECT_EQ(v.type(), nString);
    }
}

// ── Test 3: FiveSlots_ColdWarm_Hit (deterministic) ───────────────────────────
//
// Basic cold→warm cycle for the 5-file multi-binding-let expression.
// Verifies that the cache correctly handles 5 dep slots across two sessions
// and produces a warm hit on the second evaluation with no mutations.
TEST_F(EvalTraceProperty_MultiBindingLet, FiveSlots_ColdWarm_Hit)
{
    auto fileA = std::make_shared<TempTextFile>("file_a_content");
    auto fileB = std::make_shared<TempExtFile>("json", R"({"alpha": "value_b"})");
    auto fileC = std::make_shared<TempExtFile>("json", "[5, 10, 15]");
    auto fileD = std::make_shared<TempTextFile>("file_d_content");
    auto fileE = std::make_shared<TempExtFile>("json", R"({"gamma": "value_e"})");

    std::string nixCode =
        "let\n"
        "  a = builtins.readFile " + fileA->path.string() + ";\n"
        "  b = (builtins.fromJSON (builtins.readFile " + fileB->path.string() + ")).\"alpha\";\n"
        "  c = builtins.length (builtins.fromJSON (builtins.readFile " + fileC->path.string() + "));\n"
        "  d = builtins.readFile " + fileD->path.string() + ";\n"
        "  e = (builtins.fromJSON (builtins.readFile " + fileE->path.string() + ")).\"gamma\";\n"
        "in { inherit a d; value = b; count = c; extra = e; }.a";

    // Cold eval: record trace with all 5 deps.
    {
        auto cache = makeCache(nixCode);
        auto v = forceRoot(*cache);
        EXPECT_EQ(v.type(), nString);
    }

    // Warm eval: should hit cache with 0 loader calls (all 5 deps unchanged).
    {
        int calls = 0;
        auto cache = makeCache(nixCode, &calls);
        auto v = forceRoot(*cache);
        EXPECT_EQ(calls, 0)
            << "warm cache hit expected: none of the 5 files changed";
        EXPECT_EQ(v.type(), nString);
    }
}

